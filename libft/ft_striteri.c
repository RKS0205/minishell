#include "libft.h"

void	*ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int		a;

	a = 0;
	while (*s != '\0')
	{
		f(a, s);
		s++;
		a++;
	}
	*s = '\0';
	return (0);
}
